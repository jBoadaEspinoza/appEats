package com.maabi.myapplication.models;

public class Estados {
    private int id;
    private String actualmente;
    private String hace;
    private String accion;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getActualmente() {
        return actualmente;
    }

    public void setActualmente(String actualmente) {
        this.actualmente = actualmente;
    }

    public String getHace() {
        return hace;
    }

    public void setHace(String hace) {
        this.hace = hace;
    }

    public String getAccion() {
        return accion;
    }

    public void setAccion(String accion) {
        this.accion = accion;
    }
}
